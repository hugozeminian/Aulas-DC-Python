# Tuplas
# É uma variável que consegue guardar em sua memória múltiplos itens
tuplas = ('item3', 'item2', 'item3', 'item1', 'item1')
print(tuplas)
print(len(tuplas))
print(type(tuplas))

print(tuplas.count('item1')) # identificar quantas vezes o item se repete

'''
Diferença entre tuplas e listas
- ambas podem guardar múltiplos itens
- ambas possuem ordens (index)
- ambas podem receber elementos repetidos
- porém a tupla é imutável e a lista é mutável
'''