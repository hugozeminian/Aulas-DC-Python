# listas em Python são como vetores

lista = ['chicago', 'queens', 'salvador', 'pernambuco']
print(lista)

lista2 = [1, 2, 3, 10, 12]
print(lista2)

lista3 = [2.0, 3.5, 6.3]
print(lista3)

lista4 = [True, False]
print(lista4)

# index    0        1       2     3    4  -> ou seja, possui 5 elementos
lista5 = [True, 'chicago', 2.5, False, 4]
print(lista5)

print(type(lista))
print(type(lista2))
print(type(lista3))
print(type(lista4))
print(type(lista5))

# toda lista possui um índice.
print(lista5[2])
